#learn object oriented programming
class Dog:
    def bark(self):
        print("Whooaf!!!")
    def sit(self):
        print("The dog is sitting")
fido=Dog()
fido.bark()
fido.sit()
class Person:
    def talk(self):
        print("Hello world!!")
    def walk(self):
        print("The person is walking")
john=Person()
john.talk()
john.walk()